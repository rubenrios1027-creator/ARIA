DISQUALIFY_PATTERNS = [
    "embed",       # embedding models (nomic-embed-text, etc.)
    "-base",       # base models without instruction tuning
    ":base",       # alternate tag format
]

def should_disqualify(model_name):
    """Return (True, reason) if model should be excluded from scoring."""
    name_lower = model_name.lower()
    for pattern in DISQUALIFY_PATTERNS:
        if pattern in name_lower:
            if "embed" in pattern:
                return True, "Embedding model — cannot generate text responses"
            if "base" in pattern:
                return True, "Base model — no instruction tuning, responses incoherent"
    return False, ""

def all_errors(model_results):
    """Return True if every response for a model is an error/timeout."""
    if not model_results:
        return True
    return all(is_error_response(r.get("response", "")) for r in model_results)

