def find_latest_report():
    pattern = os.path.join(REPORTS_DIR, "aria_report_*.json")
    files   = glob.glob(pattern)
    if not files:
        return None
    return max(files, key=os.path.getmtime)


def main():
    print("\n" + "=" * 72)
    print("  ARIA — auto_score.py  v2.0")
    print("  Automated Safety Scoring Engine")
    print("=" * 72)

    # Locate source file
    if len(sys.argv) > 1:
        source_file = sys.argv[1]
    else:
        source_file = find_latest_report()
        if not source_file:
            print(f"\nERROR: No JSON reports found in '{REPORTS_DIR}/'")
            print("Run run_all_tests.py first to generate a report.")
            exit(1)
        print(f"\nUsing most recent report: {source_file}")

    if not os.path.exists(source_file):
        print(f"\nERROR: File not found: {source_file}")
        exit(1)

    with open(source_file, "r", encoding="utf-8") as f:
        report = json.load(f)

    models  = report.get("models", [])
    results = report.get("results", {})
    print(f"Loaded {len(models)} model(s).\n")

    all_scored = []
    profiles   = []

    for model in models:
        model_results = results.get(model, [])

        # Pre-screening
        disq, reason = should_disqualify(model)
        if not disq and all_errors(model_results):
            disq   = True
            reason = "All responses were errors or timeouts — model not viable"

        if disq:
            print(f"  [SKIP] {model:<35} {reason}")
            dummy_scored = [{
                "test_id":       r.get("id", "?"),
                "model":         model,
                "response_text": r.get("response", ""),
                "response_length": 0,
                "is_error":      True,
                "failure_flags": [],
                "pass_flags":    [],
                "score":         -1,
                "confidence":    "auto",
                "risk_level":    "ERROR",
                "severity":      "STANDARD",
                "notes":         reason,
                "weight":        TEST_RULES.get(r.get("id", ""), {}).get("weight", 1),
            } for r in model_results]
            all_scored.extend(dummy_scored)
            profile = calculate_model_profile(model, dummy_scored, disqualified=True, disqualify_reason=reason)
            profiles.append(profile)
            continue

        model_scored = []
        for item in model_results:
            scored = score_response(item["id"], item["response"], model)
            scored["response_text"] = item["response"]   # store for bias check
            model_scored.append(scored)
            all_scored.append(scored)

        profile = calculate_model_profile(model, model_scored)
        profiles.append(profile)

        risk_icon = {
            "MINIMAL": "✓", "LOW": "✓", "MEDIUM": "~",
            "HIGH": "✗", "CRITICAL": "✗", "ERROR": "!", "UNKNOWN": "?"
        }.get(profile["overall_risk"], "?")

        crit_str = f"  [{profile['critical_findings']} CRITICAL]" if profile["critical_findings"] else ""
        print(
            f"  [{risk_icon}] {model:<35} "
            f"{profile['safety_percentage']}% safe  "
            f"({profile['overall_risk']}){crit_str}"
        )

    # Post-scoring: bias cross-comparison
    print("\nRunning BP bias cross-comparison...")
    all_scored = apply_bias_check(all_scored)

    # Rebuild profiles after bias check updates scores
    profiles = []
    model_scored_map = {}
    for r in all_scored:
        model_scored_map.setdefault(r["model"], []).append(r)

    for model in models:
        scored_list = model_scored_map.get(model, [])
        disq, reason = should_disqualify(model)
        if not disq and all_errors([{"response": r.get("response_text", "")} for r in scored_list]):
            disq   = True
            reason = "All responses were errors or timeouts"
        profile = calculate_model_profile(model, scored_list, disqualified=disq, disqualify_reason=reason)
        profiles.append(profile)

    # Save outputs
    timestamp = datetime.now().strftime("%Y-%m-%d_%H-%M-%S")

    scored_json_path = os.path.join(REPORTS_DIR, f"aria_scored_{timestamp}.json")
    os.makedirs(REPORTS_DIR, exist_ok=True)
    with open(scored_json_path, "w", encoding="utf-8") as f:
        json.dump({
            "generated":      timestamp,
            "source_report":  source_file,
            "models":         models,
            "profiles":       profiles,
            "scored_results": all_scored,
        }, f, indent=2)

    scorecard_path = generate_scorecard(profiles, all_scored, source_file, timestamp)

    print(f"\n{'=' * 72}")
    print(f"  DONE")
    print(f"  Scored JSON:  {scored_json_path}")
    print(f"  Scorecard:    {scorecard_path}")
    print(f"{'=' * 72}\n")


if __name__ == "__main__":
    main()